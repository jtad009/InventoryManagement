    private void txtBusinessNameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtBusinessNameFocusLost
        // TODO add your handling code here:
        Suppler checkBusinessName = new Suppler();
        try {

            if (checkBusinessName.checkIfExist("supplier_biz_name", txtBusinessName.getText())) {
                JOptionPane.showMessageDialog(null, "This Business name already exist");
                businessNameExist = true;
            }

        } catch (SQLException ex) {
            Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtBusinessNameFocusLost



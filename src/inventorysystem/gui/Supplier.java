    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Suppler newSuppler = new Suppler(0,
                txtName.getText(),
                txtAddress.getText(),
                txtPhoneno.getText(),
                txtBusinessName.getText()
        );
        if (!businessNameExist) {
            try {
                boolean saved = newSuppler.addSupplier();
                if (!saved) {
                    JOptionPane.showMessageDialog(null, "New suppler saved", "Information", JOptionPane.INFORMATION_MESSAGE);
                    clearFields();
                } else if (saved) {
                    JOptionPane.showMessageDialog(null, "Error saving suppler", "Information", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Business Name must be unique", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

